package javaexcercisebook;

public class Default_value_Datatypes {
	static byte b1;
	static int i1;
	static short s1;
	static long l1;
	static boolean v1;
	static float f1;
	static double d1;

	public static void main(String[] args) {
		System.out.println("Default Values are :");
		System.out.println("value of byte : "+b1);
		System.out.println("value of int : "+i1);
		System.out.println("value of short : "+s1);
		System.out.println("value of long : "+l1);
		System.out.println("value of boolean : "+v1);
		System.out.println("value of float : "+f1);
		System.out.println("value of double : "+d1);
}}